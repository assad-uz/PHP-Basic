<?php
/*
trim() ফাংশনের কাজ হলো স্ট্রিং-এর শুরু ও শেষে থাকা অপ্রয়োজনীয় স্পেস (whitespace), ট্যাব, নিউলাইন (\n), ক্যারেজ রিটার্ন (\r) — এগুলো কেটে ফেলা।
*/

$text = "   Hello World!   ";

$clean = trim($text);

echo ">>" . $clean . "<<";

// Output: >>Hello World!<<

// এখানে শুরু ও শেষে থাকা স্পেস গুলো trim() কেটে দিয়েছে।
?>